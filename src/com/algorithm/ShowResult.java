package com.algorithm;

public class ShowResult {
	long displayResultLong(long[] array){
		for(int i = 0; i < array.length; i++){
			System.out.println(array[i] + " ");
		}
		
		return 0;
	}
	///////////////////////////////////
	int displayResultInt(int[] array){
		for(int i = 0; i < array.length; i++){
			System.out.println(array[i] + " ");
		}
		
		return 0;
	}
	
}
